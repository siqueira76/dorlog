/**
 * Busca medicamentos do usu√°rio por Firebase UID
 */
async function fetchUserMedications(userId: string): Promise<any[]> {
  console.log(`üíä Iniciando busca otimizada de medicamentos para ${userId}...`);
  
  const medicationsData: any[] = [];
  
  try {
    // Direct query using Firebase UID
    const optimizedQuery = query(collection(db, 'medicamentos'), where('usuarioId', '==', userId));
    const results = await getDocs(optimizedQuery);
    
    results.forEach((doc) => {
      const medicamento = doc.data();
      medicationsData.push({
        nome: medicamento.nome || 'Medicamento n√£o especificado',
        posologia: medicamento.posologia || 'Posologia n√£o especificada',
        frequencia: medicamento.frequencia || 'N√£o especificada',
        medicoId: medicamento.medicoId || '',
        source: 'firestore'
      });
    });
    
    console.log(`‚úÖ Encontrados ${results.size} medicamento(s)`);
    return medicationsData;
  } catch (error) {
    console.error(`‚ùå Erro na busca de medicamentos:`, error);
    return [];
  }
}

